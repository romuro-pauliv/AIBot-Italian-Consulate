# | Imports |----------------------------------------------------------------------------------------------------------|
import json
from typing import Any
# |--------------------------------------------------------------------------------------------------------------------|

def read_json(path: str) -> dict[str, Any]:
    with open(path, 'r') as file:
        dict_file: dict[str, Any] = json.load(file)
        file.close()
    return dict_file